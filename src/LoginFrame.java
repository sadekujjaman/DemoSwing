    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed

        String accountString = accountNoField.getText();
        String pasString = String.valueOf(passwordField.getPassword());

        if (accountString == null || accountString.length() < 7
                || pasString == null || pasString.length() < 7
                || !WriteAndReadFromFile.searchFromFile("src/files/members.txt", accountString)
                || !WriteAndReadFromFile.searchFromFile("src/files/memberspass.txt", pasString)) {
            JOptionPane.showMessageDialog(this, "Incorrect Account No or Password.\nPlease Give Valid Information or\n Create A account", "Error", JOptionPane.ERROR_MESSAGE);

        } else {
            switch (type) {
                case "About":

                    break;

                case "AccountInfo":

                    AccountInfoFrame af = new AccountInfoFrame(accountString, pasString);
                    af.setVisible(true);
                    break;

                case "Deposit":

                    DepositFrame df = new DepositFrame(accountString, pasString);
                    df.setVisible(true);
                    break;

                case "Withdraw":

//                    WithdrawFrame wf = new WithdrawFrame();
//                    wf.setVisible(true);
                    
                    break;
            }
        }

        if (rememberCheck.isSelected()) {
            System.out.println("Selected Remember...");
            Preferences prefs = Preferences.userRoot().node("saju/juniper");
//
//       String accountNo = prefs.get("accountNo", "unknown");
//       String password = prefs.get("password", "unknown");

            prefs.put("accountNo", accountString);
            prefs.put("password", pasString);

        }

        dispose();


    }//GEN-LAST:event_loginButtonActionPerformed

