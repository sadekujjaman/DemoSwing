    private void setPasswordFieldFocusListener() {
        passwordField.addFocusListener(new FocusListener() {
            @Override
            public void focusGained(FocusEvent e) {
                if (String.valueOf(passwordField.getPassword()).equals("Password")) {
                    passwordField.setText("");
                    passwordField.setForeground(Color.BLACK);
                }
            }

            @Override
            public void focusLost(FocusEvent e) {
                if (String.valueOf(passwordField.getPassword()).equals("")) {
                    passwordField.setText("Password");
                    passwordField.setForeground(Color.GRAY);
                }
            }
        });

    }

    private void setUserNameFieldFocusListener() {
        accountNoField.addFocusListener(new FocusListener() {
            @Override
            public void focusGained(FocusEvent e) {
                if (accountNoField.getText().equals("Account No")) {
                    accountNoField.setText("");
                    accountNoField.setForeground(Color.BLACK);
                }

            }

            @Override
            public void focusLost(FocusEvent e) {
                if (accountNoField.getText().isEmpty()) {
                    accountNoField.setForeground(Color.GRAY);
                    accountNoField.setText("Account No");
                }
            }
        });
    }

